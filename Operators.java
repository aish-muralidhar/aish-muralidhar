class Operators
{   
    public static void main(String args[])
    {
        int a=5,b=20;
        int c;
        c=a+b;
        System.out.println("Result after addition:"+c);
        c=a-b;
        System.out.println("Result after subtraction:"+c);
        c=a*b;
        System.out.println("Result after multiplication:"+c);
        c=b/a;
        System.out.println("Result after divison:"+c);
        c=b%a;
        System.out.println("Result after using modulus operator:"+c);
    }
}
        