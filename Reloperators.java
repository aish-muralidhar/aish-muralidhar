class Reloperators
{
    public static void main(String args[])
    {
        int x=5,y=6;
        boolean r=x<y;
        System.out.println(r);
        boolean s=x>y;
        System.out.println(s);
        //x=7;
        boolean t=x<=y;
        System.out.println(t);
        boolean u=x>=y;
        System.out.println(u);
        boolean v=x!=y;
        System.out.println(v);
        boolean w=x==y;
        System.out.println(w);
    }
}