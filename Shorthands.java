class Shorthands
{
        public static void main(String args[])
        {
                int a=10;
                a+=2;
                System.out.println("Result after applying a+=2:"+a);
                a-=2;
                System.out.println("Result after applying a-=2:"+a);
                a*=2;
                System.out.println("Result after applying a*=2:"+a);
                a/=2;
                System.out.println("Result after applying a/=2:"+a);
                a%=2;
                System.out.println("Result after applying a%=2:"+a);
                //Increment
                a++;
                System.out.println("Result after applying a++:"+a);
                
        }
}